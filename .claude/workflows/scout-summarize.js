export const meta = {
  name: 'scout-summarize',
  description: 'Route C / S2: fan out one Sonnet summarizer per scouted video, deterministically',
  whenToUse: 'The overdub-scout skill S2 step. Needs args {ids: [...], root: "D:\\\\code\\\\overdub"}.',
  phases: [
    { title: 'Summarize', detail: 'one Sonnet sub-agent per video, all spawned at once' },
  ],
}

// WHY THIS EXISTS. S2 used to be six Agent calls the orchestrator emitted itself, and it emitted
// them one per message no matter how the skill was worded. Measured over three runs on the same
// 6-video queue:
//
//   run 1  prompt 21,507 chars   spawn gap 103 s   spawn total 514 s   wave 842 s
//   run 2  prompt 19,329 chars   spawn gap  86 s   spawn total 428 s   wave 647 s
//   run 3  prompt 23,689 chars   spawn gap 123 s   spawn total 614 s   wave 774 s
//
// Two things fall out of that table. The spawn cadence tracks PROMPT SIZE (~8.5 s per 1000
// chars) because the orchestrator generates the whole prompt token by token, once per video.
// And the wave is almost exactly `spawn total + the last agent's own window` — every other
// agent finishes inside the shadow of the next spawn, so agent-side speedups were worth zero.
//
// Run 3 is the proof that wording cannot fix it: the orchestrator explicitly reasoned "spawning
// six sub-agents in a single message", announced it, and then emitted six messages anyway.
//
// A workflow removes both costs at once. `parallel()` is deterministic fan-out — it does not
// depend on a model choosing to emit N tool_use blocks — and the prompt is assembled by this
// script rather than generated, so its length costs nothing.

// Accept `args` as either an object or a JSON string. The tool's docs are explicit that the
// CALLER must pass real JSON values ("a stringified list reaches the script as one string, so
// args.filter/args.map throw"), and the runtime does exactly that — it hands over whatever it
// was given. But on the first real invocation the caller stringified it in all 8 attempts
// (2026-07-21 transcript), which is evidently the easy mistake to make, and the failure mode is
// an empty fan-out that throws a message about ids being missing when the real problem is the
// argument's shape. So: parse it, and keep passing an object from the skill anyway.
//
// A failed parse becomes null on purpose — that falls through to the empty-ids throw below,
// which is loud. Silently defaulting to the whole queue would be the dangerous repair here.
let ARGS = args
if (typeof ARGS === 'string') {
  try { ARGS = JSON.parse(ARGS) } catch (e) { ARGS = null }
}
const ROOT = (typeof ARGS === 'object' && ARGS && ARGS.root) || 'D:\\code\\overdub'
const IDS = (typeof ARGS === 'object' && ARGS && Array.isArray(ARGS.ids)) ? ARGS.ids : []

if (!IDS.length) {
  throw new Error(
    'scout-summarize: args.ids is empty. Pass the RESUME-FILTERED list from S2, e.g. ' +
    '{ids: ["abc12345678"], root: "D:\\\\code\\\\overdub"}. Refusing to run: an empty fan-out ' +
    'would report success having summarized nothing.')
}

// The prompt. Kept HERE rather than passed in `args` on purpose: anything in args has to be
// generated by the orchestrator, which is the cost this workflow exists to remove. For the same
// reason the sub-agent READS the viewer profile off disk instead of receiving it pasted in — it
// is 16.8k chars, 71% of what the old prompt weighed.
//
// Markdown backticks are deliberately absent from this template: it lives inside a JS template
// literal, and a hundred hand-escaped backticks is a syntax error waiting to happen. Only the
// decoration changed; the wording is the verified prompt from the skill.
function summarizerPrompt(id) {
  const dir = ROOT + '\\work\\' + id
  return `Your FIRST action, before reading anything: create the empty marker file
${dir}\\scout.started

Its timestamp is how the pipeline measures how long YOUR run took. Do not write a timestamp INTO
the file and do not report your own runtime anywhere: the filesystem stamps it, you only touch
it. If you skip this, the video simply has no per-video timing — never invent one.

Write both output files with PowerShell. Do NOT reach for the Write tool — it is blocked for
sub-agents ("Subagents should return findings as text, not write report files"). That guardrail
is aimed at agents dumping their own reports to disk; these two files are pipeline artifacts that
build_scout.py consumes, but the block does not know the difference, and an agent that tries
Write first loses ~45 s discovering this. Use exactly this shape — UTF-8 WITHOUT BOM in both
cases, because build_scout reads them with json.loads and a BOM breaks it:

    $summary = @'
    ...the ~200-word Russian prose, verbatim...
    '@
    [System.IO.File]::WriteAllText("${dir}\\summary.md", $summary,
      (New-Object System.Text.UTF8Encoding($false)))

    $obj = @{ quality = "low"; one_liner = $oneLiner; highlight = $highlight; paragraph = $paragraph }
    [System.IO.File]::WriteAllText("${dir}\\scout.draft.json",
      ($obj | ConvertTo-Json -Depth 5), (New-Object System.Text.UTF8Encoding($false)))

Build the JSON with a hashtable and ConvertTo-Json, never by hand — PowerShell then owns the
escaping of quotes and newlines inside your prose. In a here-string the closing '@ MUST sit at
column 0 on its own line, and nothing inside it is interpolated.

MANDATORY FIRST READ: ${ROOT}\\.claude\\viewer-profile.md — the profile of the person deciding
what to watch. Judge relevance against it and quote nothing from it back into the summary. If
that file is missing or unreadable, STOP and return the single line PROFILE-MISSING as your whole
answer — do not fall back to grading on generic quality, because a summary that silently lost the
profile is indistinguishable from one that used it, and that is the failure this instruction
exists to prevent.

You are a triage summarizer for the overdub pipeline. Read
${dir}\\sentences.json (list of {id, text, start, end} — the COMPLETE English transcript, in
order) and write ${dir}\\summary.md: a summary in RUSSIAN of about 200 words. The reader has NOT
watched the video and is deciding whether to. So answer two things, in prose: (a) is this worth
watching at all, and for whom — say so plainly, including "смотреть не стоит" if that is the
honest read; (b) what is the single most interesting thing in it / what to look out for, and
roughly where (use the start timestamps, M:SS). Ground every claim in the transcript — do not
invent facts, names, or numbers that are not there, and if the transcript is too garbled or thin
to judge, say that instead of guessing. Plain paragraphs only: no markdown headings, no bullet
lists, no title, no preamble like "Вот краткое содержание" — the file's whole content is the
summary text. Read the file in one pass; write it in one pass.

Also read ${dir}\\source.info.json — the yt-dlp metadata sidecar. Take title, channel,
upload_date (YYYYMMDD) and description from it. The transcript alone carries none of these, and
without them a profile's staleness rules and any author rule are dead letters. Treat description
as the author's own framing, i.e. promotional: useful for what the video CLAIMS to be, never as
evidence that it delivers. If the file is absent or carries only a title, say so in the paragraph
and do not infer an age — an invented upload date is worse than an acknowledged gap.

Then write ${dir}\\scout.draft.json, a JSON OBJECT (not a list) with these keys:

- quality — one of exactly "high" / "medium" / "low". Judge the MATERIAL, not the reader. Three
  things and only these three: substance (is there real information, with mechanism, numbers,
  method — or is it one tip padded out), currency (is what it shows still true, judged from
  upload_date and from what it demonstrates), and delivery (density, structure, whether the
  presenter knows the subject). high = strong on all three. medium = solid but undercut by one of
  them. low = fails on substance, or is superseded, or the delivery makes it not worth extracting
  from. Do NOT factor in whether this particular person should watch it — a well-made video on a
  topic they do not need is still well made. When torn, choose the lower grade and name the
  reason in highlight.

- author — OPTIONAL, "trusted" or "new". Emit it ONLY if the profile carries a non-empty list of
  trusted authors and you can match channel against it. With no such list there is nothing to
  compare against: omit the key entirely rather than labelling everything "new", which would add
  a column of one repeated value.

- one_liner — ONE sentence in Russian, what the video is about. It goes in a table cell; keep it
  under ~140 characters and do not restate the title.

- highlight — ONE sentence in Russian: the most interesting or useful thing IN the video, plus
  what decided the grade. A different question from one_liner and it must not repeat it:
  "разбор оркестрации агентов" says what the video is, "замеры с описанной методологией и разбор
  случаев, где схема ломается" says what you would actually get out of it. For a low, name the
  concrete defect rather than a mood. Under ~200 characters. Add "требует концентрации" here when
  the video needs undivided attention (a deep dive you have to follow with practice, rather than
  something that survives being background listening). This is the ONE field where the viewer
  profile is allowed to steer: what counts as interesting and what is already known to this
  reader come from it. The GRADE does not.

- paragraph — the full write-up in Russian, what is actually covered and why it earned that
  grade. Name the concrete thing that decided it (what specifically is dated, thin or strong).
  This is what the person reads when the one-liner interests them. Split it into 2-3 paragraphs
  separated by a BLANK LINE, where the meaning turns — the renderer honours blank lines and never
  invents its own, so an unsplit block simply renders as one paragraph.

When both files are on disk, verify they exist and return the single line OK — your final text is
a status, not the summary. The summary's home is the file.`
}

phase('Summarize')
log(`fanning out ${IDS.length} summarizers (all at once, not one per turn)`)

const results = await parallel(IDS.map((id) => () =>
  agent(summarizerPrompt(id), {
    label: `scout:${id}`,
    phase: 'Summarize',
    model: 'sonnet',            // explicit: the route was verified on Sonnet (DECISIONS 07-18/19)
    agentType: 'general-purpose',
  }).then((text) => ({ id, text: String(text || '').slice(0, 200) }))
))

// A null is an agent the runtime dropped; PROFILE-MISSING is the abort above. Both are reported
// by id rather than counted, because the operator's next move is per video: respawn this one.
const done = []
const failed = []
IDS.forEach((id, i) => {
  const r = results[i]
  if (!r || /PROFILE-MISSING/i.test(r.text)) failed.push(id)
  else done.push(id)
})

log(`summarized ${done.length}/${IDS.length}` + (failed.length ? ` — FAILED: ${failed.join(', ')}` : ''))

// The workflow does NOT run build_scout.py: a script has no filesystem or shell access, and the
// helper has to run per video after this returns anyway. Artifacts on disk are the contract here;
// this return value is a worklist, not a result.
return { done, failed, total: IDS.length }
